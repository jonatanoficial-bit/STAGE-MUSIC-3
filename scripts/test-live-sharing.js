const assert=require('assert');
const path=require('path');
const core=require(path.join(__dirname,'..','js','live-sharing-core.js'));
assert.match(core.generateCode(()=>0.25),/^[A-Z2-9]{3}-[A-Z2-9]{3}$/);
assert.strictEqual(core.normalizeCode(' abc 123 '),'ABC-123');
assert.strictEqual(core.normalizeCode('live-ab12'),'LIVE-AB12');
const songs={s1:{id:'s1',title:'Lugar Secreto',artist:'Gabriela Rocha',key:'C',bpm:'72',content:'[Refrão]\nC G Am F\nTu és o meu lugar',capo:0}};
const source={id:'list1',name:'Culto',songs:[{songId:'s1',duration:5,rehearsed:true}]};
const snapshot=core.snapshotSetlist(source,id=>songs[id]);
assert.strictEqual(snapshot.songs.length,1);
assert.strictEqual(snapshot.songs[0].content,songs.s1.content);
assert.strictEqual(snapshot.songs[0].title,'Lugar Secreto');
const resolved=core.resolveRoomSetlist({sharedSetlist:snapshot,setlistId:'missing'},[]);
assert.strictEqual(resolved.name,'Culto');
assert.strictEqual(resolved.songs[0].content,songs.s1.content);
const url=core.buildShareUrl('abc123','https://example.com/app/index.html');
assert.strictEqual(url,'https://example.com/app/convite.html?room=ABC-123');
assert.ok(core.payloadBytes({sharedSetlist:snapshot})<850000);
console.log('LIVE SHARING CORE: APROVADO');
